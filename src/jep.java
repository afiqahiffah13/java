
//pembelajaran: Enhanced Loop

public class jep{
    public static void main(String[] args){
        int lala[] = {45, 67, 89, 39, 20};
        int sum=0;

        for(int x:lala){  //identifier   // x data type
            sum += x;
        }
        System.out.println("Total of the sum is: " +sum);
    }
}

