public class switchStatement {
  public static void main(String[] args) {
    
    int umur = 2;

    switch(umur) {
        case 1:
           System.out.println("adik boleh meniarap");
           break;
        case 2:
           System.out.println("adik boleh berdiri");
           break;
        case 3:
           System.out.println("adik boleh berjalan");
           
    }
  }  
}
